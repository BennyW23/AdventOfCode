    CAST (SUBSTR(value, 6, 1) == "B" AS INTEGER) * 2 +
    CAST (SUBSTR(value, 7, 1) == "B" AS INTEGER) * 1
    AS row,
    CAST (SUBSTR(value, 8, 1) == "R" AS INTEGER) * 4 +
    CAST (SUBSTR(value, 9, 1) == "R" AS INTEGER) * 2 +
    CAST (SUBSTR(value, 10, 1) == "R" AS INTEGER) * 1
    AS col
FROM passes
),
my_row AS (
SELECT row FROM boarding_passes
GROUP BY row
HAVING COUNT() ==
    (SELECT min(count_value) FROM
        (SELECT COUNT(*) count_value FROM boarding_passes GROUP BY row
        HAVING row != (SELECT row FROM boarding_passes ORDER BY row asc LIMIT 1)
        AND row != (SELECT row FROM boarding_passes ORDER BY row desc LIMIT 1)))
),
sequence(num) AS (
SELECT 1
UNION SELECT 2
UNION SELECT 3
UNION SELECT 4
UNION SELECT 5
UNION SELECT 6
UNION SELECT 7
)

